void initsnake()
{
    location loc;
    loc.r=2;
    loc.c=10;
    for (int i=0; i<=4; i++)
    {
        snakebody.push(loc);
        board[loc.r][loc.c]=1;
        loc.r=loc.r+1;
    }
}
location getnextfire()
{
    location nextfireloc =firelocation;
    switch (firedirection)
    {
    case UP:
        nextfireloc.r=nextfireloc.r -1;
        break;
    case DOWN:
        nextfireloc.r +=1;
        break;
    case LEFT:
        nextfireloc.c -=1;
        break;
    case RIGHT :
        nextfireloc.c +=1;
        break;


    }
    return nextfireloc;

}
location getnexthead()
{
    location head =snakebody.back();
    switch (SnakeDirection)
    {
    case UP:
        head.r=head.r -1;
        break;
    case DOWN:
        head.r +=1;
        break;
    case LEFT:
        head.c -=1;
        break;
    case RIGHT :
        head.c +=1;
        break;


    }
    return head;

}
