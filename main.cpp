void movefire()
{
    if (isfirestarted)
    {
       location nextfireloc= getnextfire();
       if (nextfireloc.r > 63  ||  nextfireloc.r <0||
            nextfireloc.c > 140  ||  nextfireloc.c <0)
       {
           stopfire();
           return;
       }

       drawfire(nextfireloc.r,nextfireloc.c);
       erasefire(firelocation.r,firelocation.c);
       firelocation=nextfireloc;
    }
}



